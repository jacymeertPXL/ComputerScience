public class Scanner {

    private Scanner scanner;

    public Scanner getScanner() {
        return scanner;
    }

    public void setScanner(Scanner scanner) {
        this.scanner = scanner;
    }

    public String readline(String naam){
        return naam;
    }

    public int readint(int leeftijd){
        return leeftijd;
    }
}
